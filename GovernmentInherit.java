public class GovernmentInherit {
    String name;
    int age;
    int noOfTimesElected;
    int netWorth;
    
    public GovernmentInherit(String name, int age, int noOfTimesElected, int netWorth){
        this.name = name;
        this.age = age;
        this.noOfTimesElected = noOfTimesElected;
        this.netWorth = netWorth;
    }
}